#include <iostream>
#include <string>
#include <fstream>
int main(int argc, char* argv[])
{
  using namespace std;
  if(int(argc) == 1)
    {
      char filename[100];
      cout << "Wellcome to Dirt Editor" << endl;
      cout << "Enter name of the file to edit: ";
      cin >> filename;
      ofstream editfile;
      editfile.open(filename);
      editfile << "This text is generated by Dirt editor!" << endl;
      editfile.close();
    }
  else if(int(argc) == 2)
    {
      string filename;
      filename = argv[1];
      cout << "Editing file: " << filename << endl;
      ofstream editfile;
      editfile.open(argv[1]);
      editfile << "This text is generated by Dirt editor!" << endl;
      editfile.close();
    }
  else if(int(argc) > 2)
    {
      cout << "Please use: ./dirt [filename]" << endl;
    }
}
